-- 집합-KMK.sql
-- 합집합, 합+교집합, 교집합, 차집합
-- 집합 연산을 하려면 두 테이블의 구조가 동일해야 함

CREATE TABLE T1(
	VAL NUMBER
);
CREATE TABLE T2(
	VAL NUMBER
);

INSERT INTO T1 VALUES(1);
INSERT INTO T1 VALUES(2);
INSERT INTO T1 VALUES(3);
INSERT INTO T1 VALUES(4);

INSERT INTO T2 VALUES(3);
INSERT INTO T2 VALUES(4);
INSERT INTO T2 VALUES(5);
INSERT INTO T2 VALUES(6);

COMMIT;

SELECT * FROM T1;
SELECT * FROM T2; 

--합집합 (UNION)
SELECT * FROM T1
UNION 
SELECT * FROM T2;

-- 합집합 + 교집합(UNION ALL)
SELECT * FROM T1
UNION ALL
SELECT * FROM T2;

-- 교집합 (INTERSECTION)
SELECT * FROM T1
INTERSECT
SELECT * FROM T2;

-- 차집합 (MINUS)
SELECT * FROM T1
MINUS
SELECT * FROM T2;
SELECT * FROM T2
MINUS
SELECT * FROM T1;


















